package DBClasses;

public class Doctor {
    public String name;
    public String pagerNum;
    public String email;
    public String username;
    public String password;

    public Doctor(String name, String pagerNum, String email, String username, String password){
        this.name=name;
        this.pagerNum=pagerNum;
        this.email=email;
        this.username=username;
        this.password=password;
    }


}
